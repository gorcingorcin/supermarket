    private void sales()//ova metoda ima 3 SQL querrija objasnjena ispod: upisuje u sales i sales_prduct + azurira kolicinu
    {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        String cashier =txtSeller.getText();
        
        String subtotal = txtSubtotal.getText();
        String payed = txtPayed.getText();
        String balance = txtBalance.getText();
        int lastInsertedId = 0;
        // 1 ovaj SQL upisuje podatke u tabelu Sales 
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket", "root", "administrator");
            String querry = "insert into sales(date,cashier, subtotal, pay, balance) values (?,?,?,?,?)";
            insert = con1.prepareStatement(querry, Statement.RETURN_GENERATED_KEYS);
            insert.setString(1, date);// izmjenjeno je ime varijable
            insert.setString(2, cashier);
            insert.setString(3, subtotal);
            insert.setString(4, payed);
            insert.setString(5,balance) ;
            
            insert.executeUpdate();
           //We need last inserted object's ID
           
           ResultSet generatedKey = insert.getGeneratedKeys();
           
           if(generatedKey.next())
           {
               lastInsertedId = generatedKey.getInt(1);              
           }
           
           //2 ovaj kod upisuje podatke u tabelu sales_product i prikazuje stavke u JTabeli
           int rows =table_category.getRowCount();
           
           String querry2 = "insert into sales_product(sales_id,product_id,sell_price,quantity, total) values (?,?,?,?,?)";
           insert = con1.prepareStatement(querry2);
           
           
           String productId="";
           String price="";
           String quantity="";
           Double total=0.0;
           //ispis podataka u tabelu sa umnoškom
           for(int i = 0; i<table_category.getRowCount(); i++)
           {
               productId=(String)table_category.getValueAt(i, 0);
               price=(String)table_category.getValueAt(i, 2);
               quantity=(String)table_category.getValueAt(i, 3);
               total=(Double)table_category.getValueAt(i, 4);
               Double roundTotal = Math.round(total+100)/100.0;//zaokruživanje 2 decimale
               
               insert.setInt(1,lastInsertedId);
               insert.setString(2,productId);
               insert.setString(3,price);
               insert.setString(4,quantity);
               insert.setDouble(5,roundTotal);
               insert.executeUpdate();
               
           }
           
           //3 Ovaj SQL oduzima prodatu kolicinu od ukupne kolicine
            String querry3 = "update product set qty =qty-? where barcode=?";
           insert = con1.prepareStatement(querry3);
           
           for(int i = 0; i<table_category.getRowCount(); i++)
           {
               productId=(String)table_category.getValueAt(i, 0);
               quantity=(String)table_category.getValueAt(i, 3);
               
               
               insert.setString(1,quantity);
               insert.setString(2,productId);
               insert.execute();
               
           }
           
           
           
           insert.addBatch();
           JOptionPane.showMessageDialog(this,"Record saved");
           
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PoS.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PoS.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    }
