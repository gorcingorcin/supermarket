     public print(String subtotal, String payed, String balance, TableModel tableModel) throws PrinterException {
        initComponents();
        
        this.lSubtotal=subtotal;
        this.lPayed=payed;
        this.lBalance=balance;
        
        
        txtPrint.setText(txtPrint.getText()+"************************************************************\n");
         txtPrint.setText(txtPrint.getText()+"*****************SUPERMARKET*******************************\n");
         txtPrint.setText(txtPrint.getText()+"\n");
         txtPrint.setText(txtPrint.getText()+"PRODUCT"+"\t"+"PRICE"+"\t"+"TOTAL"+"\n");
         
         for(int i =0; i<tableModel.getRowCount();i++)
         {
             String product = (String) tableModel.getValueAt(i,1);
             String price = (String) tableModel.getValueAt(i,2);
             Double total = (Double) tableModel.getValueAt(i,4);
             
             txtPrint.setText(txtPrint.getText()+product +"\t"+price+"\t"+total+"\n");
         }
            txtPrint.setText(txtPrint.getText()+"\n");
            txtPrint.setText(txtPrint.getText()+"\n");
            txtPrint.setText(txtPrint.getText()+"\n");
            
            
            txtPrint.setText(txtPrint.getText()+"            "+"SUBTOTAL : "+ subtotal+    "\n");
          txtPrint.setText(txtPrint.getText()+"            "+"PAYED : "+ payed+    "\n");
          txtPrint.setText(txtPrint.getText()+"            "+"REST : "+ balance+    "\n");
          
          txtPrint.setText(txtPrint.getText()+"************************************************************\n");
         txtPrint.setText(txtPrint.getText()+"THANK YOU & SEE YOU SOON AGAIN \n");
         
         
         txtPrint.print();
    }
    
